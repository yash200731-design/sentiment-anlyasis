// 3. Train custom model dynamically!
app.post("/api/model/train", (req: Request, res: Response) => {
  try {
    const { dataset, testRatio = 0.2 } = req.body;
    let epochsLogs;

    if (dataset && Array.isArray(dataset) && dataset.length >= 10) {
      // Validate structure
      const validDataset = dataset.every(item => 
        item && 
        typeof item.text === "string" && 
        ["positive", "negative", "neutral"].includes(item.sentiment)
      );
      if (!validDataset) {
         res.status(400).json({ error: "Invalid dataset format. Must be an array of objects with 'text' and 'sentiment' (positive/negative/neutral)." });
         return;
      }
      epochsLogs = mlPipeline.train(dataset, testRatio);
    } else {
      // Reset to default dataset
      epochsLogs = mlPipeline.train(defaultDataset, testRatio);
    }

    const updatedStats = mlPipeline.getStats();
    res.json({
      success: true,
      message: "Model trained successfully!",
      stats: updatedStats
    });
  } catch (err: any) {
    console.error("Training endpoint error:", err);
    res.status(500).json({ error: err.message });
  }
});

// 4. Bulk upload reviews (CSV Upload processed into JSON array)
app.post("/api/bulk-analyze", (req: Request, res: Response) => {
  try {
    const { items } = req.body; // Array of { text, source, tag }

    if (!items || !Array.isArray(items) || items.length === 0) {
       res.status(400).json({ error: "An array of items is required." });
       return;
    }

    // Process all and compile database insertion models
    const recordsToSave = items.map(item => {
      const text = item.text || "";
      const analysis = mlPipeline.analyze(text);
      return {
        text,
        sentiment: analysis.sentiment,
        score: analysis.score,
        confidence: analysis.confidence,
        source: item.source || "CSV Upload",
        tag: item.tag || "Bulk Data"
      };
    });

    const savedRecords = SentimentHistoryDB.addMany(recordsToSave);

    res.json({
      success: true,
      count: savedRecords.length,
      records: savedRecords
    });
  } catch (err: any) {
    console.error("Bulk analysis endpoint error:", err);
    res.status(500).json({ error: err.message });
  }
});

