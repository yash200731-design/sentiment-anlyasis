// 1. Analyze single text
app.post("/api/analyze", async (req: Request, res: Response) => {
  try {
    const { text, generateSuggestions = false, source = "Single Text", tag = "General" } = req.body;

    if (!text || typeof text !== "string") {
       res.status(400).json({ error: "Text field is required and must be a string." });
       return;
    }

    // Run ML pipeline
    const analysis = mlPipeline.analyze(text);

    // Save to historical database record
    const saved = SentimentHistoryDB.add({
      text,
      sentiment: analysis.sentiment,
      score: analysis.score,
      confidence: analysis.confidence,
      source,
      tag
    });

    // Generate AI suggestions through Gemini if requested and API Key is available
    let aiSuggestions: string | null = null;
    const client = getGeminiClient();
    
    if (generateSuggestions && client) {
      try {
        const prompt = `You are an expert NLP Sentiment Analysis Assistant. 
Analyze the customer's text: "${text}".
The custom mathematical model classified this as ${analysis.sentiment.toUpperCase()} sentiment with a confidence of ${(analysis.confidence * 100).toFixed(1)}% and score of ${analysis.score.toFixed(2)} (-1 representing totally negative, +1 representing totally positive).

Provide exactly 3 highly actionable, bulleted suggestions or takeaways concerning how a business or writer should react to this feedback. Keep the suggestions short, specific, and professional. Do NOT include markdown introduction or conclusion lines; start directly with the bullet points.`;

        const response = await client.models.generateContent({
          model: "gemini-3.5-flash",
          contents: prompt,
        });

        aiSuggestions = response.text || "No suggestions generated by AI.";
      } catch (gemIniError: any) {
        console.error("Gemini API Error:", gemIniError);
        aiSuggestions = `Failed to generate suggestions via Gemini: ${gemIniError.message || gemIniError}`;
      }
    } else if (generateSuggestions) {
      aiSuggestions = "Gemini API key is not configured in Secrets settings. Suggestions are disabled.";
    }

    res.json({
      id: saved.id,
      timestamp: saved.timestamp,
      analysis,
      aiSuggestions
    });
  } catch (err: any) {
    console.error("Single analysis endpoint error:", err);
    res.status(500).json({ error: err.message || "An error occurred during analysis." });
  }
});

